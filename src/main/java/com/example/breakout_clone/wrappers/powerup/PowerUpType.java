package com.example.breakout_clone.wrappers.powerup;

/**
 * Enum to define the different types of PowerUp
 * @author Praket Chavan
 */
public enum PowerUpType {
    /**
     * Defines the score type power up, that provides the user with extra points
     */
    SCORE,
    /**
     * Defines the extra life type power up, that provides the user with an
     * additional lige
     */
    EXTRA_LIFE,
    /**
     * Defines the most powerful type of power up that allows the user to
     * skip a level by destroying gall the bricks
     */
    NUCLEAR,
}
